#ifndef TYPES_H
#define TYPES_H

/**
 * Signed integers are prefixed with i. Unsigned are prefixed with u.
 */

// FIXME: These typedefs are written for 32-bit X86.
typedef unsigned int   u32;
typedef          int   i32;
typedef unsigned short u16;
typedef          short i16;
typedef unsigned char  u8;
typedef          char  i8;


#endif
